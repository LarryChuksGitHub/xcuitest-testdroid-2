import Foundation

class operations {
    
    func Addition(_ number1:Int, number2:Int)->Int {
        let Result = number1 + number2
        return Result
        
    }

    func Subtraction(_ number1:Int, number2:Int)->Int {
        let Result = number1 - number2
        return Result
        
    }


}

